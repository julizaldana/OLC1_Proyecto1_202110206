    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // BOTON PARA PODER ANALIZAR ENTRADA JSON - LUEGO MANDARLO A UN TABLA HASH PARA ALMACENAR SIMBOLOS
        try {
        // Se crea un BufferedReader del contenido del jTextArea1
        BufferedReader bufferedReader = new BufferedReader(new StringReader(jTextArea1.getText()));

        // Se inicializan el analizador lexico y sintactico
        LexicoJson lexer = new LexicoJson(bufferedReader);
        SintacticoJson parser = new SintacticoJson(lexer);

        // Parse the input
        parser.parse();

        // Imprimir resultados en consola
        String result = "";
        for (int i = 0; i < parser.resultados.size(); i++) {
            result += parser.resultados.get(i) + '\n';
        }
        System.out.println(result);
    } catch (Exception ex) {
        Logger.getLogger(Maininterface.class.getName()).log(Level.SEVERE, null, ex);
        System.out.println("Error fatal en compilación de entrada.");
        // Print the error message
        System.out.println("Causa: " + ex.getMessage());
    }
              
        
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // BOTON PARA PODER ANALIZAR ENTRADA STATPY Y TRADUCIR A PYTHON
        
    try {
        // Get the content of jTextArea2 and store it in a variable
        String textoOriginal = jTextArea2.getText();

        // Create a BufferedReader from the content of jTextArea2
        BufferedReader bufferedReader = new BufferedReader(new StringReader(textoOriginal));

        // Create an instance of your parser and lexer classes
        AnalizadorStatpy.SintacticoStatpy parse;
        parse = new AnalizadorStatpy.SintacticoStatpy(new AnalizadorStatpy.LexicoStatpy(bufferedReader));

        // Parse the input
        parse.parse();

        // Get the translation results from the parser
        ArrayList<String> translationResults = parse.getTranslationResults();

        // Clear jTextArea3 before displaying the results
        jTextArea3.setText("");

        // Use a StringBuilder to store and display the results
        StringBuilder resultBuilder = new StringBuilder();

        for (String result : translationResults) {
            resultBuilder.append(result).append('\n');
        }

        // Convert the result to a string
        String result = resultBuilder.toString();

        // Display the results in jTextArea3
        jTextArea3.setText(result);

        // Optionally, you can restore the original content of jTextArea2
        // jTextArea2.setText(textoOriginal);
    } catch (Exception ex) {
        Logger.getLogger(Maininterface.class.getName()).log(Level.SEVERE, null, ex);
        System.out.println("Error fatal en la compilación de entrada.");
        // Print the error message
        System.out.println("Causa: " + ex.getMessage());
    }   
        
        
        
    }//GEN-LAST:event_jButton4ActionPerformed

