    private void jMenuItem1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem1ActionPerformed
        // ABRIR ARCHIVOS: TIPO JSON o StatPy

    // Crear un objeto JFileChooser
    JFileChooser fileChooser = new JFileChooser();

    // Configurar los filtros para que solo se muestren archivos con las extensiones .json y .sp
    FileNameExtensionFilter jsonFilter = new FileNameExtensionFilter("Archivos JSON (*.json)", "json");
    FileNameExtensionFilter statpyFilter = new FileNameExtensionFilter("Archivos StatPy (*.sp)", "sp");
    fileChooser.setFileFilter(jsonFilter);
    fileChooser.addChoosableFileFilter(statpyFilter);

    // Mostrar el cuadro de diálogo de selección de archivo
    int result = fileChooser.showOpenDialog(this);

    // Si el usuario selecciona un archivo
    if (result == JFileChooser.APPROVE_OPTION) {
        // Obtener el archivo seleccionado
        File selectedFile = fileChooser.getSelectedFile();

        // Leer el contenido del archivo y mostrarlo en el JTextArea correspondiente
        try (BufferedReader reader = new BufferedReader(new FileReader(selectedFile))) {
            StringBuilder content = new StringBuilder();
            String line;
            while ((line = reader.readLine()) != null) {
                content.append(line).append("\n");
            }
            
            // Actualizar el JTextArea correspondiente según el panel actualmente visible
            if (panelActual.equals("jsonPanel")) {
                jTextArea1.setText(content.toString());
            } else if (panelActual.equals("statPyPanel")) {
                jTextArea2.setText(content.toString());
            }
        } catch (IOException e) {
            e.printStackTrace();
        }
    }
    
    }//GEN-LAST:event_jMenuItem1ActionPerformed

